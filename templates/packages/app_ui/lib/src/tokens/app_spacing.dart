class AppSpacing {
  /// Very extra small spacing (**2.0**). Used for tight grouping of related elements.
  static const xxs = 2.0;

  /// Extra small spacing (**4.0**). Used for tight grouping of related elements.
  static const xs = 4.0;

  /// Small spacing (**8.0**). Default spacing between related small components.
  static const sm = 8.0;

  /// Medium spacing (**16.0**). Standard gutter for padding and margins.
  static const md = 16.0;

  /// Large spacing (**24.0**). Used for sections or larger visual breaks.
  static const lg = 24.0;

  /// Extra large spacing (**32.0**). Used for significant layout separations.
  static const xl = 32.0;

  /// Very extra large spacing (**40.0**). Used for significant layout separations.
  static const xxl = 40.0;

  /// Default padding for screen edges to maintain consistent safe area margins.
  ///
  /// Value: [md] (**16.0**)
  static const screen = md;
}
